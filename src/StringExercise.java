public class StringExercise {
    public static void main(String[] args) {
        // #1 - String basics
        String lyrics = "We don't need no education";
        System.out.println(lyrics);

        System.out.println(lyrics.replace("education", "thought control"));

        System.out.println("Check \"this\" out!, \"s inside of \"s!");

        System.out.println("In windows, the main drive is usually C:\\");

        System.out.println("I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!");
    }
}
